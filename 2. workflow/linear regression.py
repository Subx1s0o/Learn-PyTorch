# Лінійна регресія: рівняння y = w * x + b

# Пояснення змінних:
# y - залежна змінна (вихідний результат, наприклад, зарплата)
# x - незалежна змінна (вхідні дані, наприклад, кількість проданих товарів)
# w - коефіцієнт (показує, як сильно змінюється y при зміні x)
# b - зсув (початкове значення y, коли x дорівнює 0)

# Дані
x1, y1 = 10, 1500  # Точка 1: 10 товарів = 1500 грн
x2, y2 = 20, 2500  # Точка 2: 20 товарів = 2500 грн

# Обчислюємо коефіцієнт w
w = (y2 - y1) / (x2 - x1)  # Формула для w
print(f"Коефіцієнт (w): {w}")  # Виведе 100

# Обчислюємо зсув b
b = y1 - w * x1  # Підставляємо дані у формулу b = y - w * x
print(f"Зсув (b): {b}")  # Виведе 500

# Остаточне рівняння:
# y = w * x + b
print(f"Рівняння лінійної регресії: y = {w} * x + {b}")

# Передбачення значень
x_new_1 = 25  # Наприклад, продавець продав 25 товарів
y_new_1 = w * x_new_1 + b
print(f"Якщо продано {x_new_1} товарів, зарплата буде: {y_new_1} грн")  # Виведе 3000

x_new_2 = 50  # Наприклад, продавець продав 50 товарів
y_new_2 = w * x_new_2 + b
print(f"Якщо продано {x_new_2} товарів, зарплата буде: {y_new_2} грн")  # Виведе 5500
